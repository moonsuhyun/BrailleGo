#ifndef BSP_STM32F103_MEMORYMAP_H_
#define BSP_STM32F103_MEMORYMAP_H_

/*
 * STM32F103RB RAM Layout
 * RAM: 0x20000000 - 0x20004FFF (Total: 20KB)
 */

#define RAM_START	0x20000000
#define RAM_SIZE	(20 * 1024)
#define RAM_END		(RAM_START + RAM_SIZE)

#define SYS_STACK_SIZE		1024	// Main stack: 1KB

#define SYS_STACK_TOP    	RAM_END  // 0x20005000
#define SYS_STACK_BASE   	(SYS_STACK_TOP - SYS_STACK_SIZE) // 0x20004C00

#define TASK_STACK_TOP		SYS_STACK_BASE	// 0x20004C00
#define TASK_STACK_AREA_SIZE 10240
#define TASK_STACK_SIZE		1024
#define MAX_TASK_NUM		(TASK_STACK_AREA_SIZE / TASK_STACK_SIZE)


#endif // BSP_STM32F103_MEMORYMAP_H_

